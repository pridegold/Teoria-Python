name = "damian Lopez Peduzzi"

print(len(name)) #Cantidad de caracteres del string
print(name.find("n")) #En que posicion se encuentra la letra que busco
print(name.capitalize()) #Mayuscula a la primera letra del string, pero el resto queda en minuscula.
print(name.upper()) #Devuelve el string completo en mayuscula
print(name.lower()) #Devuelve el string completo en minuscula
print(name.isdigit()) #para saber si es int devuelve true or false
print(name.count("z")) #contar cuantos caracteres hay de la letra buscamos
print(name.replace("z", "a")) #reemplazar letras

