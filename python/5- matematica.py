import math


pi = 3.14
x = 1
y = 2
z = 3
print(round(pi)) #sirve para redondear el numero de donde este mas cerca, en este caso seria 3

print(math.ceil(pi)) # ---> esto redondea para el numero mas grande siguiente, en este caso 4
print(math.floor(pi)) # ---> esto redondea para el numero mas chico siguiente, en este caso 3
print(abs(pi)) # ---> te da el numero absoluto
print(pow(3+6+pi, 2)) # ---> elevar el cuadrado
print(math.sqrt(420)) # ---> devuelte raiz cuadrada
print(max(x, y, z)) # ---> devuelve el valor mas alto
print(min(x, y, z))  # ---> devuelve el valor mas bajo